int uniquePathsWithObstacles(int** obstacleGrid, int obstacleGridSize, int* obstacleGridColSize) 
{
    int row = obstacleGridSize;
    int column = *obstacleGridColSize;
    int i, j;

    if (obstacleGrid[0][0]) {
        return 0; //第一个点都是障碍物
    }
    obstacleGrid[0][0] = 1;
    for (i = 1; i < row; i++) {
        if (obstacleGrid[i][0] == 0 && obstacleGrid[i - 1][0] == 1) {
            obstacleGrid[i][0] = 1;
        }
        else {
            obstacleGrid[i][0] = 0;
        }
    }

    for (j = 1; j < column; j++) {
        if (obstacleGrid[0][j] == 0 && obstacleGrid[0][j - 1] == 1) {
            obstacleGrid[0][j] = 1;
        }
        else {
            obstacleGrid[0][j] = 0;
        }
    }
#if 0
    for (i = 0; i < row; i++) {
        for (j = 0; j < column; j++) {
            printf("%3d", *(*(obstacleGrid + i) + j));
        }
        printf("\r\n");
    }
#endif
    for (i = 1; i < row; i++) {
        for (j = 1; j < column; j++) {
            if (obstacleGrid[i][j] == 0) {
                obstacleGrid[i][j] = obstacleGrid[i - 1][j] + obstacleGrid[i][j - 1];
            }
            else {
                obstacleGrid[i][j] = 0;
            }
        }
    }
    return obstacleGrid[row-1][column-1];
}

